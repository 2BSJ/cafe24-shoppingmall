package com.cafe24.pjshop.vo;

public class OrderVo {

	private Long no;
	private String cs_status;
	private String order_status;
	private String address;
	private String home_number;
	private String phon_number;
	private String message;
	private String member_status;
	private String password;
	private Long member_no;
	public Long getNo() {
		return no;
	}
	public void setNo(Long no) {
		this.no = no;
	}
	public String getCs_status() {
		return cs_status;
	}
	public void setCs_status(String cs_status) {
		this.cs_status = cs_status;
	}
	public String getOrder_status() {
		return order_status;
	}
	public void setOrder_status(String order_status) {
		this.order_status = order_status;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getHome_number() {
		return home_number;
	}
	public void setHome_number(String home_number) {
		this.home_number = home_number;
	}
	public String getPhon_number() {
		return phon_number;
	}
	public void setPhon_number(String phon_number) {
		this.phon_number = phon_number;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getMember_status() {
		return member_status;
	}
	public void setMember_status(String member_status) {
		this.member_status = member_status;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Long getMember_no() {
		return member_no;
	}
	public void setMember_no(Long member_no) {
		this.member_no = member_no;
	}
	
}
